int	ft_iterative_power(int nb, int power)
{
	int	i;
	int	temp;

	if (power < 0)
	{
		return (0);
	}
	else if (power == 0)
	{
		return (1);
	}
	else
	{
		i = 0;
		temp = 1;
		while (i < power)
		{
			temp = temp * nb;
			i++;
		}
	}
	return (temp);
}
